def extract_text_from_docx(path):
    """Wyciąga tekst z pliku DOCX — akapity i tabele."""
    try:
        doc = docx.Document(path)
        fragmenty = []

        # Iterujemy po elementach dokumentu w kolejności ich występowania
        for element in doc.element.body:
            tag = element.tag.split('}')[-1]  # 'p' dla akapitu, 'tbl' dla tabeli

            if tag == 'p':
                # Akapit
                para = docx.text.paragraph.Paragraph(element, doc)
                if para.text.strip():
                    fragmenty.append(para.text)

            elif tag == 'tbl':
                # Tabela — wyciągamy każdy wiersz jako wiersz tekstu
                tabela = docx.table.Table(element, doc)
                for wiersz in tabela.rows:
                    komorki = [k.text.strip() for k in wiersz.cells if k.text.strip()]
                    if komorki:
                        fragmenty.append(" | ".join(komorki))

        return "\n".join(fragmenty)
    except Exception as e:
        return f"[Błąd odczytu DOCX: {e}]"

def pobierz_czcionke():
    """Pobiera PEŁNĄ czcionkę DejaVuSans wspierającą wszystkie polskie znaki narodowe."""
    font_path = "DejaVuSans_Full.ttf"
