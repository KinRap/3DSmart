def wyczysc_raport(tekst):
    """Usuwa bloki kodu i markdown z raportu narracyjnego."""
    import re
    tekst = re.sub(r'```json.*?```', '[Dane przetworzone przez agenta]', tekst, flags=re.DOTALL)
    tekst = re.sub(r'```.*?```', '', tekst, flags=re.DOTALL)
    tekst = re.sub(r'^#{1,6}\s+', '', tekst, flags=re.MULTILINE)
    tekst = re.sub(r'\*\*\*(.+?)\*\*\*', r'\1', tekst, flags=re.DOTALL)
    tekst = re.sub(r'\*\*(.+?)\*\*', r'\1', tekst, flags=re.DOTALL)
    tekst = re.sub(r'\*(.+?)\*', r'\1', tekst, flags=re.DOTALL)
    tekst = re.sub(r'\n{3,}', '\n\n', tekst)
    return tekst.strip()

def wyczysc_znaki_pdf(tekst):
    """Zastępuje emoji i symbole, których czcionka DejaVuSans nie obsługuje."""
    zamiany = {
        '✅': '[OK]',      '❌': '[BLAD]',     '⚠️': '[UWAGA]',
        '🔴': '[KRYT]',    '🟡': '[OSTRZ]',    '🟢': '[OK]',
        '🏢': '',          '💰': '',           '🛠️': '',
        '⚡': '',          '→': '->',          '\u00a0': ' ',
        '\u2019': "'",     '\u201c': '"',      '\u201d': '"',
        '•': '*',          '▪': '*'
    }
    for znak, zamiennik in zamiany.items():
        tekst = tekst.replace(znak, zamiennik)
    bezpieczne_linie = []
    for linia in tekst.split('\n'):
        czysta_linia = "".join([c for c in linia if ord(c) < 65536])
        bezpieczne_linie.append(czysta_linia)
    return "\n".join(bezpieczne_linie)

def generuj_pdf(tresc, nazwa_pliku, tytul_przetargu):
    font_path = pobierz_czcionke()
    pdf = FPDF()
    pdf.add_page()
    if font_path:
        pdf.add_font('UniFont', '', font_path)
        pdf.set_font('UniFont', '', 14)
    else:
        pdf.set_font('Helvetica', 'B', 14)
    pdf.set_text_color(56, 189, 248)
    tytul_safe = wyczysc_znaki_pdf(tytul_przetargu).upper()
    pdf.multi_cell(0, 10, f"ANALIZA: {tytul_safe}",
                   new_x=XPos.LMARGIN, new_y=YPos.NEXT, align='C')
    pdf.ln(5)
    pdf.set_text_color(0, 0, 0)
    if font_path:
        pdf.set_font('UniFont', '', 10)
    else:
        pdf.set_font('Helvetica', '', 10)
    tresc_safe = wyczysc_znaki_pdf(tresc)
    pdf.multi_cell(0, 6, tresc_safe)
    pdf.output(nazwa_pliku)
    files.download(nazwa_pliku)


